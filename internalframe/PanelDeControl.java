    private void dibujar() {
        Color colorInicial = consigueColorInicial();
        Color colorFinal = consigueColorFinal();        
        zonaDeDibujo.repaint();
        int ramificaciones = (int) (jSpinnerRamificaciones.getValue());
        int grosor = (int) (jSpinnerGrosor.getValue());
        int iteraciones = (int) (jSpinnerNivelesDeRama.getValue());
        float proporcion = (float) (jSpinnerProporcion.getValue());
        float  velocidad = (float)(jSpinnerVelocidad.getValue());
        
        int rojoInicial = colorInicial.getRed();
        int verdeInicial = colorInicial.getGreen();
        int azulInicial = colorInicial.getBlue();
        
        int incrementoRojo = (colorFinal.getRed() - colorInicial.getRed()) / iteraciones;
        int incrementoVerde = (colorFinal.getGreen() - colorInicial.getGreen()) / iteraciones;
        int incrementoAzul = (colorFinal.getBlue() - colorInicial.getBlue()) / iteraciones;
        
        Point pInicial = new Point(
                (int)(zonaDeDibujo.getWidth()/2),
                (int)(zonaDeDibujo.getHeight() - 50)
        );
        Point pFinal = new Point(
                (int)(pInicial.x + Math.cos(Math.toRadians(90))*75*proporcion ),
                (int)(pInicial.y - Math.sin(Math.toRadians(90))*75*proporcion)                            
        );

        Graphics2D brocha = (Graphics2D) (zonaDeDibujo.getGraphics());
        brocha.setColor(colorInicial);
        brocha.setStroke(new BasicStroke(grosor));
        
        new Pintor(90, pInicial, pFinal, ramificaciones, grosor,
                iteraciones, proporcion/2 + proporcion/4,
                velocidad, brocha, rojoInicial + incrementoRojo, incrementoRojo, verdeInicial + incrementoVerde,
                incrementoVerde, azulInicial + incrementoAzul, incrementoAzul).start();
                
    }

